package com.naresh.dsa.basic.array;

public class OneDimArray {
public static void main(String[] args) {
	int[] numbers = new int[5];
	numbers[0] = 20;
	numbers[1] = 10;
	numbers[2] = 12;
	numbers[3] = 4;
	numbers[4] = 4;
	for (int i = 0; i < numbers.length; i++) {
		System.out.println(numbers[i]);
		
	}
}
}
